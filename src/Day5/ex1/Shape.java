package Day5.ex1;

public class Shape {
    private String type;
    private int param;

    public Shape(String type, int param) {
        this.type = type;
        this.param = param;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getParam() {
        return param;
    }

    public void setParam(int param) {
        this.param = param;
    }
}




